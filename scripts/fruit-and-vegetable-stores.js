let url = `https://dunzo-clone-masai.herokuapp.com/fruit`;
async function FetchApi(){
    try {
        
        let res = await fetch(url);
        let data = await res.json()
        console.log(data)
        showItems(data)
    } catch (error) {
        console.log(error)
    }
}
